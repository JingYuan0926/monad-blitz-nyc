/**
 * Memonads — the general contract for the Memonads hotel on Monad Testnet
 * (chainId 10143). Handles reception registration + on-chain memories,
 * MON credits (10,000 credits = 1 MON), expert session payments, and
 * on-chain reviews. Chat content itself stays off-chain.
 */
import { keccak256, toHex } from "viem";

export const MEMONADS_ADDRESS =
  "0x09513cAf4430C1D50cBaB00a637Db63E5810F844" as const;

/** Price of one credit in wei. topUp() reverts unless msg.value is a positive multiple. */
export const WEI_PER_CREDIT = BigInt("100000000000000"); // 1e14

/**
 * Experts in the hotel are NPCs without wallets; each gets a stable
 * pseudo-address derived from its id so payments and reviews can be
 * recorded on-chain against it.
 */
export function expertAddress(id: string): `0x${string}` {
  return ("0x" + keccak256(toHex(`memonads:expert:${id}`)).slice(-40)) as `0x${string}`;
}

